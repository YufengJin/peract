"""peract IL evaluation client: WebSocket → RLBench rollouts → success rate.

Purpose
-------
Connects to a policy server that speaks the `policy_websocket` protocol and
runs N RLBench episodes against it, aggregating success rate and mean
return. Each step sends a minimal obs dict, receives an 8-D end-effector
pose + gripper action, and feeds it to RLBench's `task.step()`. The
handshake guard refuses to run if the server advertises a different
`action_dim` than the 8 dims PerAct/RLBench expects.

Success criterion: RLBench returns `reward >= 1.0` or any episode
terminates with `terminate=True` (task-specific). Mirrors peract/eval.py
convention.

Logs and videos land under `<log_dir>/eval/<task>--<YYYYMMDD_HHMMSS>/eval.log`
so every run is its own auditable directory. The text log is a stdout
mirror, flushed on every write — `tail -f` friendly. Args are dumped to
the log head for reproducibility.

Video saving (`--save_video`) enables RLBench's front camera, writes one MP4
per episode to the run_dir. Requires a working offscreen GL context; the
headless compose profile sets `QT_QPA_PLATFORM=offscreen` which prevents
CoppeliaSim from creating a GL context — in that case camera init crashes
on the first reset, the script catches it, and falls back to no-video
for the remaining episodes. For real video capture, use the X11 profile
or wrap the command in `xvfb-run -a ...`.

Example
-------
    # Terminal A — start any policy_websocket server (random smoke server here;
    # swap for a trained peract policy server in real evaluation)
    python tests/test_random_policy_server.py --port 8000

    # Terminal B — run eval against it
    python scripts/run_eval.py --policy_server_addr localhost:8000 \\
        --task open_drawer --n-episodes 10 --max-steps 200

    # Grep the resulting log for failure-mode statistics
    grep InvalidAction logs/eval/open_drawer--*/eval.log | wc -l

Generated by benchmark-env-generator (IL Category).
"""
from __future__ import annotations

import argparse
import atexit
import os
import signal
import sys
from datetime import datetime
from typing import Dict, Optional

import numpy as np

from policy_websocket import WebsocketClientPolicy

SMOKE_ENV_ID = "open_drawer"
ACTION_DIM = 8


def log(msg: str, log_file: Optional["os.PathLike"] = None) -> None:
    """Print to stdout and, if open, also write to a log file (flushed each call)."""
    print(msg)
    if log_file is not None and not log_file.closed:
        log_file.write(msg + "\n")
        log_file.flush()


def build_env(task_name: str, headless: bool = True, enable_front_camera: bool = False):
    from rlbench.environment import Environment
    from rlbench.action_modes.action_mode import MoveArmThenGripper
    from rlbench.action_modes.arm_action_modes import EndEffectorPoseViaPlanning
    from rlbench.action_modes.gripper_action_modes import Discrete
    from rlbench.observation_config import ObservationConfig
    import rlbench.tasks as rlbench_tasks

    cls_name = "".join(w.capitalize() for w in task_name.split("_"))
    task_cls = getattr(rlbench_tasks, cls_name)

    obs_config = ObservationConfig()
    obs_config.set_all(False)
    obs_config.joint_positions = True
    obs_config.gripper_open = True
    if enable_front_camera:
        # Only enabled when --save_video is on. On the headless compose
        # profile (QT_QPA_PLATFORM=offscreen) querying this sensor crashes
        # CoppeliaSim — the caller wraps env.launch + task.reset in try/except
        # and falls back to no-video. Use the X11 profile or xvfb-run for
        # reliable capture.
        obs_config.front_camera.rgb = True

    env = Environment(
        action_mode=MoveArmThenGripper(EndEffectorPoseViaPlanning(), Discrete()),
        obs_config=obs_config,
        headless=headless,
    )
    env.launch()
    return env, env.get_task(task_cls)


def _obs_to_payload(obs) -> Dict:
    """Downsample RLBench Observation to a msgpack-friendly dict. Keeps payload
    small — real inference servers can demand more; this matches the random
    server's contract (ignored on server side).
    """
    payload = {}
    if getattr(obs, "joint_positions", None) is not None:
        payload["joint_positions"] = np.asarray(obs.joint_positions, dtype=np.float32)
    if getattr(obs, "gripper_open", None) is not None:
        payload["gripper_open"] = float(obs.gripper_open)
    if getattr(obs, "front_rgb", None) is not None:
        payload["front_rgb"] = np.asarray(obs.front_rgb, dtype=np.uint8)
    return payload


def main():
    parser = argparse.ArgumentParser(description="peract IL eval against a policy server.")
    parser.add_argument("--policy_server_addr", required=True, help="host:port")
    parser.add_argument("--n-episodes", type=int, default=1)
    parser.add_argument("--max-steps", type=int, default=20)
    parser.add_argument("--task", type=str, default=SMOKE_ENV_ID)
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument("--policy", type=str, default="unknown",
                        help="Policy name for the log header (free-form)")
    parser.add_argument("--log_dir", type=str, default="./logs",
                        help="Parent dir for per-run eval logs (created if missing)")
    parser.add_argument("--save_video", action="store_true",
                        help="Record RLBench front_camera and write one MP4 per episode. "
                             "Requires X11 profile or xvfb — fails on headless (QT offscreen).")
    parser.add_argument("--video_fps", type=int, default=30,
                        help="MP4 frame rate (default 30)")
    args = parser.parse_args()

    # ---- Set up run directory + log file ----
    date_str = datetime.now().strftime("%Y%m%d_%H%M%S")
    run_dir = os.path.join(args.log_dir, "eval", f"{args.task}--{date_str}")
    os.makedirs(run_dir, exist_ok=True)
    log_path = os.path.join(run_dir, "eval.log")
    log_file = open(log_path, "w")

    def _cleanup(signum=None, frame=None):
        try:
            if not log_file.closed:
                log_file.close()
        finally:
            sys.stdout.flush()
            sys.stderr.flush()
        if signum is not None:
            os._exit(1)

    signal.signal(signal.SIGINT, _cleanup)
    signal.signal(signal.SIGTERM, _cleanup)
    atexit.register(lambda: None if log_file.closed else log_file.close())

    # ---- Header: dump args ----
    log("=" * 60, log_file)
    log("peract IL eval", log_file)
    log("=" * 60, log_file)
    for k, v in sorted(vars(args).items()):
        log(f"  {k}: {v}", log_file)
    log(f"  run_dir: {run_dir}", log_file)
    log("=" * 60, log_file)
    log("", log_file)

    host, port = args.policy_server_addr.split(":")
    client = WebsocketClientPolicy(host=host, port=int(port))
    meta = client.get_server_metadata()
    log(f"server metadata: {meta}", log_file)
    if meta.get("action_dim") != ACTION_DIM:
        raise ValueError(
            f"server action_dim={meta.get('action_dim')} != expected {ACTION_DIM}")

    np.random.seed(args.seed)

    # Try to bring the env up with the front camera if --save_video; on
    # headless (QT_QPA_PLATFORM=offscreen) the GL context creation crashes
    # CoppeliaSim, so catch and fall back.
    save_video = args.save_video
    try:
        env, task = build_env(args.task, headless=True, enable_front_camera=save_video)
    except Exception as e:
        if save_video:
            log(
                f"[warn] enabling front_camera crashed env.launch ({type(e).__name__}: {e}); "
                f"disabling --save_video and retrying without camera. "
                f"Use the X11 compose profile or xvfb-run for video capture.",
                log_file,
            )
            save_video = False
            env, task = build_env(args.task, headless=True, enable_front_camera=False)
        else:
            raise

    returns, successes = [], []
    try:
        for ep in range(args.n_episodes):
            descriptions, obs = task.reset()
            ep_return = 0.0
            success = False
            frames = []
            for step in range(args.max_steps):
                payload = _obs_to_payload(obs)
                response = client.infer(payload)
                action = np.asarray(response["actions"], dtype=np.float32)
                try:
                    obs, reward, terminate = task.step(action)
                    ep_return += float(reward)
                    if reward >= 1.0:
                        success = True
                    if save_video:
                        frame = getattr(obs, "front_rgb", None)
                        if frame is not None:
                            frames.append(np.asarray(frame, dtype=np.uint8))
                    if terminate:
                        break
                except Exception as e:
                    log(
                        f"  ep {ep} step {step}: {type(e).__name__} "
                        f"(random action, expected)",
                        log_file,
                    )
            returns.append(ep_return)
            successes.append(int(success))
            running_sr = sum(successes) / len(successes) * 100
            log(
                f"ep {ep+1}/{args.n_episodes}: return={ep_return:.2f} success={success} "
                f"| running_sr={sum(successes)}/{len(successes)} ({running_sr:.1f}%)",
                log_file,
            )
            if save_video and frames:
                try:
                    import imageio
                    mp4 = os.path.join(run_dir, f"episode_{ep:03d}_success={success}.mp4")
                    writer = imageio.get_writer(
                        mp4, fps=args.video_fps, format="FFMPEG", codec="libx264")
                    for f in frames:
                        writer.append_data(f)
                    writer.close()
                    log(f"  saved video: {mp4}", log_file)
                except Exception as e:
                    log(f"[warn] imageio write failed: {type(e).__name__}: {e}", log_file)

        log("", log_file)
        log("=" * 60, log_file)
        log("FINAL RESULTS", log_file)
        log("=" * 60, log_file)
        log(f"  policy:       {args.policy}", log_file)
        log(f"  task:         {args.task}", log_file)
        log(f"  episodes:     {args.n_episodes}", log_file)
        log(f"  mean_return:  {np.mean(returns):.3f}", log_file)
        log(f"  success_rate: {np.mean(successes):.3f}", log_file)
        log(f"  log_file:     {log_path}", log_file)
        log("=" * 60, log_file)
    finally:
        try:
            client.close()
        except Exception:
            pass
        env.shutdown()
        if not log_file.closed:
            log_file.close()


if __name__ == "__main__":
    main()
